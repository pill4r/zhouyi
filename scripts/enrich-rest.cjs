function main() {
  const raw = fs.readFileSync(DATA_PATH, 'utf8')
  const data = JSON.parse(raw)

  let changedHex = 0
  let changedLines = 0
  const missingHex = []
  const missingLines = []

  for (const hex of data) {
    if (hex.id < 9 || hex.id > 64) continue
    if (MEANINGS[hex.id]) {
      hex.meaning = MEANINGS[hex.id]
      changedHex++
    } else {
      missingHex.push(hex.id)
    }
    if (!Array.isArray(hex.lines)) continue
    const plains = LINE_PLAINS[hex.id]
    if (!plains) { missingLines.push(hex.id); continue }
    for (const line of hex.lines) {
      const plain = plains[line.position]
      if (plain) {
        line.judgementPlain = plain
        changedLines++
      } else {
        missingLines.push(`${hex.id}-${line.position}`)
      }
    }
  }

  fs.writeFileSync(DATA_PATH, JSON.stringify(data, null, 2) + '\n', 'utf8')
  console.log(`✓ 已更新 ${changedHex} 卦的 meaning`)
  console.log(`✓ 已为 ${changedLines} 爻新增 judgementPlain`)
  if (missingHex.length) console.log('⚠ 缺卦义:', missingHex.join(','))
  if (missingLines.length) console.log('⚠ 缺爻辞:', missingLines.join(','))
}

main()
