function main() {
  const raw = fs.readFileSync(DATA_PATH, 'utf8')
  // 注意：原文件是 2 空格缩进的 JSON，直接用 JSON.parse 再 stringify(2) 保持格式
  const data = JSON.parse(raw)

  let changedHex = 0
  let changedLines = 0

  for (const hex of data) {
    if (hex.id < 1 || hex.id > 8) continue
    if (MEANINGS[hex.id]) {
      hex.meaning = MEANINGS[hex.id]
      changedHex++
    }
    if (!Array.isArray(hex.lines)) continue
    const plains = LINE_PLAINS[hex.id]
    if (!plains) continue
    for (const line of hex.lines) {
      const plain = plains[line.position]
      if (plain) {
        // 在 judgement 之后插入 judgementPlain
        line.judgementPlain = plain
        changedLines++
      }
    }
  }

  fs.writeFileSync(DATA_PATH, JSON.stringify(data, null, 2) + '\n', 'utf8')
  console.log(`✓ 已更新 ${changedHex} 卦的 meaning`)
  console.log(`✓ 已为 ${changedLines} 爻新增 judgementPlain`)
}

main()
